<?php

// fonction pour ramener de la BDD des (string) en tablaeu(array) pour mettre dans une liste(ul, li)
function lineToArray(string $string)
{
    return explode(PHP_EOL, $string);
}

// fonction de remplacement de tous ce qui n'est pas clean pour le web et son affichage
function slugify($text, string $divider = '-')
{
  // replace non letter or digits by divider
  $text = preg_replace('~[^\pL\d]+~u', $divider, $text);

  // transliterate
  $text = iconv('utf-8', 'us-ascii//TRANSLIT', $text);

  // remove unwanted characters
  $text = preg_replace('~[^-\w]+~', '', $text);

  // trim
  $text = trim($text, $divider);

  // remove duplicate divider
  $text = preg_replace('~-+~', $divider, $text);

  // lowercase
  $text = strtolower($text);

  if (empty($text)) {
    return 'n-a';
  }

  return $text;
}